    <h2 id="termins">Термины</h2>
    <ul>
        <li>
            мандела (женского рода) - конкретный факт об изменении реальности.
            Названа в честь Нельсона Манделы.
        </li>
        <li>
            геомандела - то же, что и мандела, только в более узком смысле,
            относящаяся к категории изменений географических карт или
            ландшафтов.
        </li>
        <li>
            личная мандела - та, которая приключались с вами, и поэтому о ней не
            прочитаешь в интернете. Например, изменилось что-то в одежде, в
            квартире или среди родственников.
        </li>
        <li>
            мандельщик - человек, подверженный действию эффекту Манделы, то есть
            тот, кто видит изменения, но при этом не считает, что это
            конфабуляции.
        </li>
        <li>
            остатки - материальные доказательства первоначального состояния
            факта до изменения, воспроизведённые человеком по памяти. Как
            правило, остатки делаются неосознанно, то есть или не зная, что факт
            уже изменился, или же создание остатка было выполнено ещё до
            изменения самого факта. Точный механизм возникновения остатков
            неизвестен.
        </li>
        <li>
            флип-флоп (англ. flip-flop; иногда называют "откат") - процесс, при
            котором изменение возвращается в первоначальное сотояние. То есть
            мандела исчезает, а большинство людей о ней забывают. Флип-флопы
            тоже иногда порождают свои остатки.
        </li>
        <li>
            манделиться - изменяться. При этом изменение оказывается в прошлом,
            словно оно всегда таким и было.
        </li>
    </ul>

