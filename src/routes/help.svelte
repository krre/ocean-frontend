    <h2 id="rules">Рекомендуемые правила оформления мандел</h2>

    <ol>
        <li>
            Есть два типа заголовка - простой и сложный.
            <ul>
                <li>
                    В простом заголовке нужно указать только тему манделы.
                    Напишите о новой версии описываемого вами факта с целью
                    привлечения внимания тех, кто увидит новую версию впервые.
                    <h4>Пример</h4>
                    <p>Мандела умер в 2013 году.</p>
                    <p>В моём доме не выражаться.</p>
                    <p>В Nutella нет полосок.</p>
                </li>
                <li>
                    В сложном заголовке присутствуют уже три поля: Что, Было,
                    Стало. Они больше подходят для мандел, в которых что-то
                    изменилось. В Что укажите, о чём идет речь. В Было - как вы
                    помните предыдущую версию факта. В Стало - как официально
                    есть сейчас.
                    <h4>Пример</h4>
                    <p>
                        Что: Прощальная речь Ельцина
                        <br />
                        Было: Я устал, я ухожу
                        <br />
                        Стало: Я ухожу
                    </p>
                    <p>В каталоге такой заголовок будет показан в виде:</p>
                    <p>Прощальная речь Ельцина: Я устал, я ухожу / Я ухожу</p>
                </li>
            </ul>
        </li>
        <li>
            В описании предоставьте дополнительную информацию о факте. Опишите
            как старую версию, так и новую.
        </li>
        <h4>Пример</h4>
        <p>
            Мандела умер в тюрьме в ЮАР в начале 90-х годов. Однако теперь он
            остался жив, вышел из тюрьмы, стал президентом ЮАР и умер в глубкой
            старости в 2013 году.
        </p>
        <li>
            Снабдите ваши пояснения изображениями. Можете показать факт в новом
            состоянии.
            <h4>Пример</h4>
            <p>Фото дожившего до седин Манделы.</p>
            А также остатки, подтверждающие, что ваша версия не выдумана.
            <h4>Пример</h4>
            <p>
                Скан старой газеты, где написано о смерти Манделы в 90-х годах.
                Либо воспоминания других людей, описывающих похороны и траур по
                Манделе на весь СССР.
            </p>
        </li>
        <li>
            Если манделу можно посмотреть на видео, то добавьте ролик с YouTube,
            демонстрирующий новую версию (старой на видео вы уже не найдёте).
            <h4>Пример</h4>
            <p>
                Речь Ельцина, где вместо "Я устал, я ухожу" он говорит "Я ухожу.
                Я сделал, всё что мог".
            </p>
        </li>
        <li>
            Если тема вашей манделы уже была где-то подробно описана, то укажите
            ссылку на это описание.
            <h4>Пример</h4>
            <p>
                <a href="https://krre31.livejournal.com/39995.html">
                    https://krre31.livejournal.com/39995.html
                </a>
            </p>
        </li>
        <li>
            Если вы нашли манделу в другом месте и точно знаете, что это место
            является первоисточником, и раньше о ней нигде не упоминалось, то
            также укажите ссылку на этот первоисточник, чтобы за вами потом не
            гонялись копирасты. Если мандела общеизвестная, то такую ссылку
            можно не указывать.
            <p />
        </li>
        <li>
            Все правила не являются обязательными. Если у вас творческий кризис,
            и вы не знаете, что писать о манделе, то и одного заголовка будет
            достаточно.
        </li>
    </ol>

