<script lang="ts">
    import { run } from 'svelte/legacy';

    interface Props {
        success?: string;
        error?: string;
    }

    let { success = "", error = $bindable("") }: Props = $props();

    run(() => {
        if (success) {
            error = "";
        }
    });
</script>

<style>
    .success {
        color: green;
    }

    .error {
        color: red;
    }
</style>

{#if success}
    <div class="success">{success}</div>
{/if}

{#if error}
    <div class="error">Ошибка: {error}</div>
{/if}
